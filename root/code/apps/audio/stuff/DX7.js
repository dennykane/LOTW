  function extractName (data,offset) {
    var offset = offset || 118;	// 118 for packed, 145 for unpacked
    var name  = "";
    for (var n = 0; n < 10; n++) {
      var c = data[n + offset];
      switch (c) {
        case  92:  c = 'Y';  break;  // yen
        case 126:  c = '>';  break;  // >>
        case 127:  c = '<';  break;  // <<
        default: if (c < 32 || c > 127) c = 32; break;
      }
    name += String.fromCharCode(c);
    }
    return name;
  }
